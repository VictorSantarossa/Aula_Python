listaCompras = ['arroz','feijao','macarrao','ovo']
print(listaCompras)

listaCompras[1] = 'milho'
print(listaCompras)

listaCompras.append('batata') #O .APPEND() Adiciona item no final da lista
print(listaCompras)

listaCompras.insert(1, 'jilo') #O .INSERT() Adiciona item em uma posição
print(listaCompras)

listaCompras.sort() #O .SORT() Ordena a lista em ordem alfabetica
print(listaCompras)

listaCompras.reverse() #O .REVERSE() Inverte a ordem dos itens
print(listaCompras)

listaCompras.remove('ovo') #O .REMOVE() Ele remove o item da lista pelo nome
print(listaCompras)

listaCompras.pop(3) #O .POP() Ele remove o item da lista pela posição
print(listaCompras)

